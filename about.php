<h1 id="red"> HALDIRAMS </h1>

<ul id="yellow">
   <li>  Haldiram’s is a famous sweet and namkeen or snacks brand from Nagpur, which now has many retail outlets in various places across India. </li>

   <li>  Haldiram’s has also been exporting it’s products to various countries like the United States of America, Australia, Canada, London, Pakistan, Bangladesh, Nigeria, UK, UAE, Zambia, Bahrain, South Africa, Hong Kong, Japan, Kenya, Libya, South Korea, Mauritius, Srilanka and Singapore. </li>
   
	 
	<li> It has many restaurants in Delhi and in Delhi NCR serving, fresh Indian food, including snacks, main course, sweets, and desserts. </li>
</ul>

<h1 id="blue"> Products </h1>
  <ul id="lightgreen">
    <li> The main  product categories in which Haldiram deals are- </li>

    <li> Fresh Sweets, consisting of Moong Barfi, Kaju Roll, Kaju Katli, Besan Laddoo, Orange Burfi, Dry Fruit laddu. </li>

     <li> Papads : Mainly consisting of typical Bikaneri Papad. </li>

    <li> Drinks, consisting of kala khatta squash, Thandai, Lemon, Mango, Badam, and Khus Squash. </li>

    <li> Continental, consisting of baked snacks Taka Tak and Fun Bitas. </li>

    <li> Bakery, has a variety of baked items like cookies, puffs, and rusks. </li>

    <li> Mopleez, consisting of Aloo laccha, chana choor, boondi, All in one, and navratna mix. </li>

    <li> Namkeens, has a huge variety of Kaju Mixture to Aloo bhujia, besan bhujia and many more. </li>

    <li> Canned Sweets, consisting of canned RajBhog, Gulab Jamun, Meetha Cubes, and Chum Chum. </li>
   </ul>
   
 <h1 id="green"> History </h1>
  <ul id="pink">
    <li> The history of Haldiram’s dates back to the late 1930’s.
    <li> In 1937, Haldiram was founded by Gangabisenji Agrawal as a retail shop of sweets and namkeen in Bikaner, Rajasthan. </li>
    <li> His dream was to grow the company into a very big empire and his dream was realized in 1970 as “Haldiram’s Nagpur”. </li>
	<li> Following this, it became a huge hit amongst consumers and Haldiram started opening many retail outlets catering to all market segments. </li>

    <li>In 1997, Haldiram entered into the market of dairy and milk products, manufacturing ghee, khowa, and butter milk. </li>
    <li>It also started producing snacks and products including 3-D snacks and vermicelli. </li>
  </ul>
  
<h1 id="darkcyan">Popular Dishes </h1>
